    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String adress = contract_address_text_field_update.getText();
        String postal_code = postal_code_text_field_update.getText();
        Object client_code_obj = client_code_combo_box_update.getSelectedItem();
        String client_code = "";
        SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
        String start_date = "";
        Object service_obj = service_type_combo_box_update.getSelectedItem();
        String service = "";
        if(contract_data_start_update.getDate() != null){
            start_date = sdf.format(contract_data_start_update.getDate().getTime());
        }

        Object value = contracts_update_combo_box.getSelectedItem();

        if(value == null)
            JOptionPane.showMessageDialog(null,"Trebuie sa selectati un contract inainte de a face o actualizare!","Eroare!",JOptionPane.WARNING_MESSAGE);
        else{
            String[] options = {"Da","Nu"};
            if(JOptionPane.showOptionDialog(null, "Esti sigur/a ca vrei sa actualizezi datele acestui contract?\n"
                + value.toString() ,null, JOptionPane.YES_NO_CANCEL_OPTION,JOptionPane.QUESTION_MESSAGE,null,options,options[1]) == 0)
        {
            if(adress.equals("")){
                adress = null;
            }
            else{
                if(adress.length() > 70)
                {
                    JOptionPane.showMessageDialog(null,"Ati depasit numaru maxim de caractere pentru campul \"adresa\"!(70 de caractere) ","Eroare!",JOptionPane.WARNING_MESSAGE);
                    return;            
                }
            }

            if(postal_code == null || postal_code.equals("") ){
                postal_code = "-1";
            }
            else{
                String pattern = "\\d+";
                Pattern r = Pattern.compile(pattern);


                if(!r.matcher(postal_code).matches()){
                    JOptionPane.showMessageDialog(null,"Cod postal incorect!","Eroare!",JOptionPane.WARNING_MESSAGE);
                    return;
                }
            }

            if(client_code_obj == null ){
                client_code = "-1";
            }
            else{
                client_code = client_code_obj.toString().split(" ")[0];
            }

            if(start_date.equals("")){
                start_date = null;
            }

            if(service_obj == null){
                service = "-1";
            }
            else
            {
                service = service_obj.toString().split(" ")[0];
            }

            oracleSQLconn.updateContract(Long.parseLong(value.toString().split(" ")[0]), adress, Integer.parseInt(postal_code), Long.parseLong(client_code), start_date, Integer.parseInt(service));
            show_contracts_buttonActionPerformed(null);
        }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

