    private void user_login_buttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_user_login_buttonActionPerformed
        if(signout_user_button.isEnabled() == false)
        {
            String pattern = "[+-]?[0-9][0-9]*";

            Pattern r = Pattern.compile(pattern);

            user_id = user_id_text_field.getText();
            if(r.matcher(user_id).find())
            {

                String user_password = user_password_field.getText();

                if(user_password.equals(""))
                {
                    JOptionPane.showMessageDialog(null,"Campul Parola trebuie sa fie completat!","Eroare!",JOptionPane.WARNING_MESSAGE);
                }

                if(oracleSQLconn.checkIfUserExists(user_id, user_password))
                {
                    JOptionPane.showMessageDialog(null,"Te-ai logat cu succes!");
                    enableComponents(jPanel16, true);
                    jTabbedPane1.setSelectedIndex(1);

                    user_id_text_field.setText("");
                    user_password_field.setText("");

                    user_logged_message.setText("Te-ai logat ca " + oracleSQLconn.getUser(Long.parseLong(user_id)).split("  ")[1] + " : " + user_id);
                    hello_message.setText("Bine ai venit, " + oracleSQLconn.getUser(Long.parseLong(user_id)).split("  ")[1] + "!");
                    user_id_message.setText("Cod client: " + user_id);
                    signout_user_button.setEnabled(true);
                    
                    user_meters_combobox.removeAllItems();
                    
                    for(String meter : oracleSQLconn.getAllMeterByUser(Long.parseLong(user_id)))
                        user_meters_combobox.addItem(meter);
                }

            }else{
                JOptionPane.showMessageDialog(null,"ID-ul trebuie sa fie un numar natural!","Eroare!",JOptionPane.WARNING_MESSAGE);
            }
        }else
        {
           JOptionPane.showMessageDialog(null,"Deja esti logat cu un cont! \nMai intai apasa butonul de delogare!","Eroare!",JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_user_login_buttonActionPerformed

