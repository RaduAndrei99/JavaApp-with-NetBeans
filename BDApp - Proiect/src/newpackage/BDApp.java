    private void insert_card_buttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_insert_card_buttonActionPerformed
        try{
            String card_number = credit_card_number_text_field.getText();
            String cvv = credit_card_cvv_text_field.getText();
            
            SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
            String expire_date = "";
            
            if(credit_card_expire_date_text_field.getDate() != null){
                expire_date = sdf.format(credit_card_expire_date_text_field.getDate().getTime());
            }
            else
            {
                JOptionPane.showMessageDialog(null,"Introduceti o data valida!","Eroare!",JOptionPane.WARNING_MESSAGE);
                return;
            }
            
            String card_holder = credit_card_card_holder_text_field.getText();

            if(card_number.equals("")){
                JOptionPane.showMessageDialog(null,"Introduceti numarul cardului!","Eroare!",JOptionPane.WARNING_MESSAGE);
                return;
            }
            else
            {
                if(card_number.length() != 16)
                {
                    JOptionPane.showMessageDialog(null,"Numarul cardului trebuie sa contina 16 cifre!","Eroare!",JOptionPane.WARNING_MESSAGE);
                    return;
                }
                
                String pattern = "\\d+";

                Pattern r = Pattern.compile(pattern);
                
                if(!r.matcher(card_number).matches())
                {
                    JOptionPane.showMessageDialog(null,"Numar card invalid!","Eroare!",JOptionPane.WARNING_MESSAGE);
                    return;
                }
            }

            if(cvv.equals("")){
                JOptionPane.showMessageDialog(null,"Introduceti codul CVV!","Eroare!",JOptionPane.WARNING_MESSAGE);
                return;
            }
            else
            {
                if(cvv.length() != 3)
                {
                    JOptionPane.showMessageDialog(null,"CVV-ul trebuie sa aiba 3 cifre!","Eroare!",JOptionPane.WARNING_MESSAGE);
                    return;
                }
                String pattern = "\\d+";

                Pattern r = Pattern.compile(pattern);
                
                if(!r.matcher(cvv).matches())
                {
                    JOptionPane.showMessageDialog(null,"CVV invalid!","Eroare!",JOptionPane.WARNING_MESSAGE);
                    return;
                }            
            }
            
            String pattern = "^[a-zA-Z]+(([,. -][a-zA-Z ])?[a-zA-Z]*)*$";

            Pattern r = Pattern.compile(pattern);

            if(!r.matcher(card_holder).matches())
            {
                JOptionPane.showMessageDialog(null,"Introduceti un nume valid!","Eroare!",JOptionPane.WARNING_MESSAGE);
                return;      
            }

            oracleSQLconn.insertNewCreditCard(Long.parseLong(card_number), Integer.parseInt(cvv), expire_date, card_holder, Long.parseLong(user_id));
            show_cards_buttonActionPerformed(null);

        }catch(Exception e){
            e.printStackTrace();
            JOptionPane.showMessageDialog(null,"Eroare necunoscuta la inserarea unui nou card de credit!","Eroare!",JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_insert_card_buttonActionPerformed

